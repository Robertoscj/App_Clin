class Patient {
  String id;
  String name;
  String email;
  String age;
  String gender;
  String phoneNumber;
  String address;
  String medicalHistory;

  Patient({
    required this.id,
    required this.name,
    required this.email,
    required this.age,
    required this.gender,
    required this.phoneNumber,
    required this.address,
    required this.medicalHistory,
  });
}
